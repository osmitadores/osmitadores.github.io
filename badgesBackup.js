function toggle_visibility(unobtr) {
   var e = document.getElementsByClassName(unobtr);

    for ( var i=0; i < e.length; i++ ) {
        if(e[i].style.display != 'none'){
            e[i].style.display = 'none';
        }else{
            e[i].style.display = 'table-row';
        }
    }
}
